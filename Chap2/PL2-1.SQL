VAR B NUMBER

DECLARE -- DECLARATION PART OF OUTER/PARENT/MAIN BLOCK
   X NUMBER(3) := 10;  -- LIFE OF X VARIABLE STARTS HERE
   Z NUMBER(3);
BEGIN -- EXECUTABLE PART OF OUTER BLOCK
   DBMS_OUTPUT.PUT_LINE('OUTER BLOCK');
   DBMS_OUTPUT.PUT_LINE('X = ' || X);
   DECLARE -- DECLARATION OF INNER BLOCK
      Y NUMBER(3) := 20; -- LIFE OF Y VARIABLE STARS HERE
   BEGIN -- EXECUTABLE PART OF INNER BLOCK
      DBMS_OUTPUT.PUT_LINE('INNER BLOCK');
	  DBMS_OUTPUT.PUT_LINE('Y = ' || Y);
      DBMS_OUTPUT.PUT_LINE('X = ' || X);
	  :B := Y;
	  Z := Y;  
   END; -- END OF INNER BLOCK, LIFE OF Y VARIABLE ENDS HERE
   DBMS_OUTPUT.PUT_LINE('OUTER BLOCK');
--   DBMS_OUTPUT.PUT_LINE('Y = ' || Y);  -- ERROR
--   DBMS_OUTPUT.PUT_LINE('Y (USING OUTER BLOCK VARIABLE) = ' || Z);  
   DBMS_OUTPUT.PUT_LINE('Y (USING BIND/HOST VARIABLE) = ' || :B);
   DBMS_OUTPUT.PUT_LINE('X = ' || X);
END; -- END OF OUTER BLOCK, LIFE OF X VARIABLE ENDS HERE
