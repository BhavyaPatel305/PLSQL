DECLARE
  V_BF BINARY_FLOAT; -- RESERVES 5 BYTES, ACCURACY OF 8 DIGITS AFTER DECIMAL POINT
  V_BD BINARY_DOUBLE; -- RESERVES 9 BYTES, ACCURACY OF 16 DIGITS AFTER DECIMAL POINT
BEGIN
  V_BF := 12.39;
  V_BD := 4.123456789123456789;
  DBMS_OUTPUT.PUT_LINE('V_BF = ' || V_BF);   -- DISPLAYED USING EXPONENT FORMAT
  DBMS_OUTPUT.PUT_LINE('V_BD = ' || V_BD);   -- DISPLAYED USING EXPONENT FORMAT
  DBMS_OUTPUT.PUT_LINE('V_BF + V_BD = ' || (V_BF+V_BD));
  DBMS_OUTPUT.PUT_LINE('V_BD = ' || TO_CHAR(V_BF+V_BD,'000.000000'));
  DBMS_OUTPUT.PUT_LINE('V_BF = ' || TO_CHAR(V_BF,'999.9999'));
  DBMS_OUTPUT.PUT_LINE('V_BD = ' || TO_CHAR(V_BD,'000.000000'));
END;
/