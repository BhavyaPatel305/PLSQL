DECLARE
   V_BI BINARY_INTEGER;  -- -2147483648 TO +2147483647 -- SUPPORTED IN ALL RDBMS PRODUCTS
   V_PI PLS_INTEGER; -- SUPPORTED IN ORACLE ONLY
BEGIN
   V_BI := 19.5;  -- ROUND OFF
   V_PI := -5;
   DBMS_OUTPUT.PUT_LINE('V_BI = ' || V_BI);
   DBMS_OUTPUT.PUT_LINE('V_PI = ' || V_PI);
   DBMS_OUTPUT.PUT_LINE('SUM     = ' || (V_BI + V_PI));
   DBMS_OUTPUT.PUT_LINE('PRODUCT = ' || V_BI * V_PI);
END;
