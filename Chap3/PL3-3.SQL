
SET SERVEROUT ON FORMAT WRA
DECLARE
	N1 NUMBER(3) := &NUMBER1;  -- 10  20  30 10
	N2 NUMBER(3) := &NUMBER2;  -- 20  10  10 30
	N3 NUMBER(3) := &NUMBER3;  -- 30  30  20 20
BEGIN
	DBMS_OUTPUT.PUT_LINE('START OF PROGRAM.');
	IF N1 > N2 AND N1 > N3 THEN
		DBMS_OUTPUT.PUT_LINE('IF -> ' || N1 || ' IS BIGGEST NUMBER.');
	ELSIF N2 > N1 AND N2 > N3 THEN
		DBMS_OUTPUT.PUT_LINE('ELSIF 1 -> ' || N2 || ' IS BIGGEST NUMBER.');
	ELSE
		DBMS_OUTPUT.PUT_LINE('ELSE -> ' || N3 || ' IS BIGGEST NUMBER.');	
	END IF;
	DBMS_OUTPUT.PUT_LINE('END OF PROGRAM.');
END;
