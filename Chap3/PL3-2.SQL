SET SERVEROUT ON
DECLARE
	N1 NUMBER(3) := &NUMBER1;  -- 10
	N2 NUMBER(3) := &NUMBER2;  -- 20
BEGIN
	DBMS_OUTPUT.PUT_LINE('START OF PROGRAM.');
	IF N1 >= N2 THEN
		/* TRUE BLOCK */
		DBMS_OUTPUT.PUT_LINE(N1 || ' IS BIG NUMBER.');
	ELSE
		/* FALSE BLOCK */
		DBMS_OUTPUT.PUT_LINE(N2 || ' IS BIG NUMBER.');	
	END IF;
	DBMS_OUTPUT.PUT_LINE('END OF PROGRAM.');
END;
